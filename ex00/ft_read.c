void	ft_read(char *str, int read_in[4][4])
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < 4)
	{
		while (j < 4)
		{
			read_in[i][j] = *str - '0';
			j++;
			str = str + 2;
		}
		j = 0;
		i++;
	}
}
